/**
 * _islower.c - A program that checks for lowercase alphabet
 * @c: The character to check
 * Description: 'This program checks for lowercase alphabets'
 * Return: Always success
*/

int _islower(int c)
{
if (c >=97 && c <= 122)
{
return (1);
}

else
{
return (0);
}

return (0);
}

